package com.netcracker.spEL;

public class SimpleConfig {
    private String name = "TAYOTA";
    private int age = 4;
    private int countOfHomeAnimals = 100;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getCountOfHomeAnimals() {
        return countOfHomeAnimals;
    }

    public void setCountOfHomeAnimals(int countOfHomeAnimals) {
        this.countOfHomeAnimals = countOfHomeAnimals;
    }


}
